export function generateImageHeroes() {
  const images: Record<string, HTMLImageElement> = {};

  for (const imageName in heroesIcons) {
    const image = new Image();
    image.width = 30;
    image.height = 30;
    image.src = heroesIcons[imageName];
    images[imageName] = image;
  }
  return images;
}

export function generateImageUltimate() {
  const images: Record<string, HTMLImageElement> = {};

  for (const imageName in heroesIcons) {
    const image = new Image();
    image.width = 20;
    image.height = 20;
    image.src = ultimateIcons[imageName];
    image.style.backgroundColor = "black"; // Définir une couleur de fond pour l'image
    image.style.objectFit = "cover"; // Assurer que l'image couvre complètement l'espace

    images[imageName] = image;
  }
  return images;
}
